#include "./includes/libft.h"

void	ft_putnbr(int n)
{
	char	*d;

	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if ((d = ft_itoa(n)))
	{
		ft_putstr(d);
		ft_strdel(&d);
	}
}
