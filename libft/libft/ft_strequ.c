#include "./includes/libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;

	i = 0;
	if (s2 == NULL && s1 == NULL)
		return (0);
	else if (s1 == NULL || s2 == NULL)
		return (1);
	while ((s1[i]) && (s2[i]))
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
	}
	if (s1[i] != s2[i])
		return (0);
	else
		return (1);
}
