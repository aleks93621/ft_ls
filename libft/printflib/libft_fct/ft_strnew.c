#include "../includes/ft_printf.h"

char	*ft_strnew(size_t size)
{
	char			*d;

	d = (char*)malloc(size + 1);
	if (d == NULL)
		return (NULL);
	ft_memset(d, (int)'\0', size + 1);
	return (d);
}
